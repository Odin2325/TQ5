# Maximum und Minimum
# Aufgabe: Lies mehrere Zahlen ein und gib das größte und kleinste Element zusammen aus.
# Beispiel: Input: "4 2 9 1" → Output: "Max=9, Min=1"

beispiel_liste = [53, 324, 64, 123, 17, 34, 7, 4, 77, 6]

kleinste_zahl = min(beispiel_liste)
groesste_zahl = max(beispiel_liste)

print(kleinste_zahl)
print(groesste_zahl)
