#User Story 1: Event erstellen und teilen

#Titel: Event-Erstellung und Teilen

#Beschreibung:
#Als registrierter Nutzer möchte ich ein neues Event erstellen und mit meinen Freunden teilen können, damit sie davon erfahren und daran teilnehmen können.

#Akzeptanzkriterien:

#Der Nutzer kann beim Erstellen eines Events Titel, Beschreibung, Datum, Uhrzeit, Ort und Bild hinzufügen.
#Der Nutzer kann auswählen, ob das Event öffentlich oder privat ist.
#Nach dem Speichern erhält der Nutzer eine Bestätigung, dass das Event erfolgreich erstellt wurde.
#Das erstellte Event wird auf dem Profil des Nutzers sowie im Feed der eingeladenen Freunde angezeigt.